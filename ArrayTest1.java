class ArrayTest{
	public static void main(String[] args){
		int [] ia= new int [3];
		ia[0]=5;
		ia[1]=6;
		ia[2]=7;
		System.out.println("ia[0]");
		System.out.println("ia[1]");
		System.out.println("ia[2]");
		int [] [] mia= new int [1] [2];
		mia[0] [0]=5;
		mia[0] [1]=6;
		mia[1] [0]=7;
		mia[1] [1]=8;
		mia[2] [0]=9;
		mia[2] [1]=10;
		System.out.println("mia[0] [0]");
		System.out.println("mia[0] [1]");
		System.out.println("mia[1] [0]");
		System.out.println("mia[1] [1]");
		System.out.println("mia[2] [0]");
		System.out.println("mia[2] [1]");
	}
}
